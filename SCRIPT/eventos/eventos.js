/* 
    Eventos no JavaScript:

    Eventos são ações que ocorrem na página, como:
    - Clicar
    - Passar o mouse
    - Digitar
    - Carregar a página
    - Redimensionar a janela
    - Scroll, entre outros.

    ----------------------------
    Eventos de Mouse:
    ----------------------------

    click         - Quando o botão do mouse é clicado (um clique)
    dblclick      - Clique duplo (dois cliques rápidos)
    mousedown     - Quando qualquer botão do mouse é pressionado
    mouseup       - Quando o botão do mouse é solto
    mousemove     - Quando o mouse se move dentro do elemento
    mouseover     - Quando o mouse passa por cima do elemento (entra)
    mouseout      - Quando o mouse sai do elemento
    mouseenter    - Quando o mouse entra no elemento (não se propaga)
    mouseleave    - Quando o mouse sai do elemento (não se propaga)
    contextmenu   - Quando o botão direito do mouse é clicado (abre o menu de contexto)
    wheel         - Quando a roda do mouse é rolada

    ----------------------------
    Eventos de Teclado:
    ----------------------------

    keydown       - Quando uma tecla é pressionada
    keyup         - Quando a tecla é liberada
    keypress      - (OBSOLETO) Era usado para pressionar teclas imprimíveis

    ----------------------------
    Eventos de Janela / Documento:
    ----------------------------

    load          - Quando a página ou um recurso é carregado
    unload        - Quando a página é descarregada (obsoleto)
    resize        - Quando a janela do navegador é redimensionada
    scroll        - Quando a página é rolada
    beforeunload  - Antes da página ser descarregada (permite confirmar saída)
    error         - Quando ocorre um erro em um elemento (imagem, script, etc.)

    ----------------------------
    Eventos de Formulário:
    ----------------------------

    submit        - Quando um formulário é enviado
    change        - Quando o valor de um input, select ou textarea muda
    input         - Quando o valor de um campo muda (em tempo real)
    focus         - Quando o elemento recebe foco
    blur          - Quando o elemento perde o foco
    reset         - Quando um formulário é resetado

    ----------------------------
    Eventos diversos úteis:
    ----------------------------

    DOMContentLoaded - Quando o HTML foi completamente carregado e processado (sem esperar CSS/imagens)
    touchstart        - Quando um toque começa (em dispositivos móveis)
    touchend          - Quando o toque termina
    touchmove         - Quando o dedo se move na tela
    pointerdown       - Combinação de mouse, toque e stylus (substitui mouse/touch)
    pointerup
    pointermove
    pointerenter
    pointerleave

*/
